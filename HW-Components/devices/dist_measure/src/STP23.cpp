bool STP23::decode(size_t len, const uint8_t *data, uint32_t rx_id)
{
  if (len != kRxDataLen) {
    decode_fail_cnt_++;
    return false;
  }

  memcpy(&data_, data, len);

  // 帧头校验
  if (data_.header != 0x54) {
    decode_fail_cnt_++;
    return false;
  }

  // CRC 校验
  crc_ = 0;
  for (uint8_t i = 0; i < kRxDataLen - 1; i++) {
    crc_ = CrcTable[(crc_ ^ data[i]) & 0xff];
  }
  if (crc_ != data_.crc8) {
    decode_fail_cnt_++;
    crc_fail_cnt_++;
    return false;
  }

  calcDistance();
  decode_success_cnt_++;
  oc_.update();
  is_updated_ = true;

  return true;
}

void STP23::calcDistance(void)
{
  float dist_sum = 0;
  float current_dist = 0;
  uint8_t dist_count = 0;

  for (int i = 0; i < 12; i++)  // 12个点取平均
  {
    if (data_.point[i].distance != 0)  // 去除0的点
    {
      dist_count++;
      dist_sum += data_.point[i].distance;
    }
  }
  if (dist_count != 0) {
    current_dist = dist_sum / dist_count;
  } else {
    current_dist = 0;
  }

  // 低通滤波
  distance_ = (1 - weight_) * distance_ + weight_ * current_dist;
}
